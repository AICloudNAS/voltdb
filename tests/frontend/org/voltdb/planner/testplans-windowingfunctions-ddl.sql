DROP TABLE AAA           IF EXISTS;
DROP TABLE BBB           IF EXISTS;
DROP TABLE AAA_PA        IF EXISTS;
DROP TABLE AAA_PB        IF EXISTS;
DROP TABLE AAA_PC        IF EXISTS;
DROP TABLE AAA_STRING    IF EXISTS;
DROP TABLE AAA_STRING_PA IF EXISTS;

CREATE TABLE AAA (
    A INTEGER,
    B INTEGER,
    C INTEGER,
);

CREATE TABLE BBB (
    A INTEGER,
    B INTEGER,
    C INTEGER,
);

CREATE TABLE AAA_PA (
    A INTEGER NOT NULL,
    B INTEGER NOT NULL,
    C INTEGER NOT NULL,
);
PARTITION TABLE AAA_PA ON COLUMN A;

CREATE TABLE AAA_STRING_PA (
    A VARCHAR NOT NULL,
    B INTEGER NOT NULL,
    C INTEGER NOT NULL,
);
PARTITION TABLE AAA_STRING_PA ON COLUMN A;

CREATE TABLE AAA_TIMESTAMP (
    A TIMESTAMP NOT NULL,
    B INTEGER   NOT NULL,
    C INTEGER   NOT NULL,
);

--
-- Create some tables with indexes on single and multiple
-- columns and indexes on single and multiple non-column
-- expressions.
--
CREATE TABLE ITBL_ONLY_A (
    A INTEGER   NOT NULL,
    B INTEGER   NOT NULL,
    C INTEGER   NOT NULL,
);
CREATE INDEX II_ITBL_ONLY_A ON ITBL_ONLY_A ( A );

CREATE TABLE ITBL_AB_COL (
    A INTEGER   NOT NULL,
    B INTEGER   NOT NULL,
    C INTEGER   NOT NULL,
);
CREATE INDEX II_ITBL_AB_COL ON ITBL_AB_COL ( A, B );

CREATE TABLE ITBL_AB_1EXPR (
    A INTEGER   NOT NULL,
    B INTEGER   NOT NULL,
    C INTEGER   NOT NULL,
);
CREATE INDEX II_ITBL1_AB_1EXPR ON ITBL_AB_1EXPR ( A + B );

CREATE TABLE ITBL_AB_2EXPR (
    A INTEGER   NOT NULL,
    B INTEGER   NOT NULL,
    C INTEGER   NOT NULL,
);
CREATE INDEX II_ITBL_AB_2EXPR ON ITBL_AB_2EXPR ( A + B, A - B );
